}

fn load(function: anytype) !void {
    function.ptr = try getProc(function.sig, function.name);
}

fn getProc(comptime T: type, name: [:0]const u8) !T {
    if (wglGetProcAddress(name)) |proc| {
        return @as(T, @ptrCast(@alignCast(proc)));
    } else if (OpenGL.lookup(T, name)) |proc| {
        return proc;
    } else {
        std.debug.print("Function {s} not found\n", .{name});
        return error.NoSuchFunction;
    }
}

fn Function(comptime T: type, str: [:0]const u8) type {
    return struct {
        ptr: Signature = undefined,
        const Self = @This();
        const Signature = T;

        fn load(self: *Self) !void {
            self.ptr = try getProc(Signature, str);
        }
    };
}

